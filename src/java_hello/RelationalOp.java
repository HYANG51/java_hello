package java_hello;

public class RelationalOp {
	public static void main(String[] args) {
		System.out.println("3 >= 2 : " + (3 >= 2));
		System.out.println("3 <= 2 : " + (3 <= 2));
		System.out.println("7.0 == 7 : " + (7.0 == 7));
		System.out.println("7.0 != 7 : " + (7.0 != 7));		// !=는 같지 않을때 true, 같으면 false가 나옴.
	}
}
